DEFAULT_LANGUAGE = "en"
LANGUAGE_ORDER = ["en", "es", "fr", "hi", "mr"]
from flask import request


def get_locale():
    lang = (request.cookies.get("lang") or DEFAULT_LANGUAGE).strip().lower()
    return lang if lang in LANGUAGES else DEFAULT_LANGUAGE


def translate(key):
    locale = get_locale()
    if key in LANGUAGES.get(locale, {}):
        return LANGUAGES[locale][key]
    return LANGUAGES[DEFAULT_LANGUAGE].get(key, key)
